const DICTS: Record<Lang, Dict> = { it, en, es, fr };

/** Translate `key` for `lang`, interpolating `{name}` params. Falls back to Italian, then
 * to the key itself, so nothing ever renders blank. */
export function translate(lang: Lang, key: string, params?: Record<string, string | number>): string {
  const d = DICTS[lang] ?? it;
  let s = d[key] ?? it[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) s = s.split("{" + k + "}").join(String(v));
  }
  return s;
}
